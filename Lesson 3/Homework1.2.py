num = 1
while 0 < num < 8:
    if num == 1:
        print("Monday")
    if num == 2:
        print("Tuesday")
    if num == 3:
        print("Wednesday")
    if num == 4:
        print("Thursday")
    if num == 5:
        print("Friday")
    if num == 6:
        print("Saturday")
    if num == 7:
        print("Sunday")
    num = num + 1
else:
    print("Something went wrong")
